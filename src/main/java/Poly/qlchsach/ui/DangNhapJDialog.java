        // TODO add your handling code here:
        txtTaiKhoan.setBackground(white);
        txtMatKhau.setBackground(white);
        if(txtTaiKhoan.getText().trim().length()>0){
            if(txtMatKhau.getPassword().length>0){
                login();
            }else{
                txtMatKhau.setBackground(red);
                MessageDialogHelper.showMessageDialog(parentForm, "Không được để trống mật khẩu!", "Thông báo");
            }
        }else{
            txtTaiKhoan.setBackground(red);
            MessageDialogHelper.showMessageDialog(parentForm, "Không được để trống tên đăng nhập", "Lỗi");
        }
