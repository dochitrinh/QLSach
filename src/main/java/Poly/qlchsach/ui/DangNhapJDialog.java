    void login() {
        
        String taiKhoan = txtTaiKhoan.getText();
        String matKhau = new String(txtMatKhau.getPassword());
        try {
            TaiKhoan nhanVien = dao.findByTaiKhoan(taiKhoan);
            if (nhanVien != null) {
                String matKhau2 = nhanVien.getMatKhau();
                if (matKhau.equals(matKhau2)) {
                    ShareHelper.USER = nhanVien;
                    MessageDialogHelper.showMessageDialog(parentForm, "Đăng nhập thành công!", "Thông báo");
                    this.dispose();
                } else {
                    MessageDialogHelper.showMessageDialog(parentForm, "Đăng nhập thất bại!", "Lỗi");
                }
            } else {
                MessageDialogHelper.showMessageDialog(parentForm, "Sai tên đăng nhập hoặc mật khẩu", "Lỗi");
            }
        } catch (Exception e) {
            MessageDialogHelper.showMessageDialog(parentForm, "Lỗi truy vấn dữ liệu", "Lỗi");
        }
    }
    
    void exit() {
        if (MessageDialogHelper.showConfirmDialog(parentForm, "Đăng nhập thành công!", "Hỏi")==JOptionPane.YES_OPTION) {
            System.exit(0);
        }
    }

