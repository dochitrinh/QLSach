    private void validation() {
        // TODO add your handling code here:
        txtTaiKhoan.setBackground(Color.white);
        txtMatKhau.setBackground(Color.white);
        txtMatKhauMoi.setBackground(Color.white);
        txtMatKhauMoi2.setBackground(Color.white);
        if (txtTaiKhoan.getText().trim().length() > 0) {
            if (txtMatKhau.getPassword().length > 0) {
                if (txtMatKhauMoi.getPassword().length > 0) {
                    if (txtMatKhauMoi2.getPassword().length > 0) {
                        doiMatKhau();
                    } else {
                        txtMatKhauMoi2.setBackground(red);
                        MessageDialogHelper.showMessageDialog(parentForm, "Không được để trống xác nhận mật khẩu", "Thông báo");
                    }
                } else {
                    txtMatKhauMoi.setBackground(red);
                    MessageDialogHelper.showMessageDialog(parentForm, "Không được để trống mật khẩu mới", "Thông báo");
                }
            } else {
                txtMatKhau.setBackground(red);
                MessageDialogHelper.showMessageDialog(parentForm, "Không được để trống xác nhận mật khẩu", "Thông báo");
            }
        } else {
            txtTaiKhoan.setBackground(red);
            MessageDialogHelper.showMessageDialog(parentForm, "Không được để trống tên đăng nhập", "Thông báo");
        }
    }

