    private void doiMatKhau() {
        String taiKhoan = txtTaiKhoan.getText();
        String matKhau = new String(txtMatKhau.getPassword());
        String matKhauMoi = new String(txtMatKhauMoi.getPassword());
        String matKhauMoi2 = new String(txtMatKhauMoi2.getPassword());
        if (!taiKhoan.equalsIgnoreCase(ShareHelper.USER.getTaiKhoan())) {
            MessageDialogHelper.showMessageDialog(parentForm, "Sai tên đăng nhập!", "Thông báo");
        } else if (!matKhau.equals(ShareHelper.USER.getMatKhau())) {
            MessageDialogHelper.showMessageDialog(parentForm, "Sai tên mật khẩu!", "Thông báo");
        } else if (!matKhauMoi.equals(matKhauMoi2)) {
            MessageDialogHelper.showMessageDialog(parentForm, "Xác nhận mật khẩu không đúng!", "Thông báo");
        } else {
            ShareHelper.USER.setMatKhau(matKhauMoi);
            dao.updateMK(ShareHelper.USER);
            MessageDialogHelper.showMessageDialog(parentForm, "Đổi mật khẩu thành công!", "Thông báo");
        }
    }
    
